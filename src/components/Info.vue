            } else {
              home.style.backgroundColor = `rgb(${newR}, ${newB}, ${newG})`;
            }

          }
        });

      }
    },
    resetBgColor: function() {
      const home = document.getElementsByClassName('home')[0];
      let r = 127;
      let g = 255;
      let b = 212;

      home.style.backgroundColor = `rgb(${r}, ${g}, ${b})`;
    }
  },
  mounted () {
    this.scrollBgColor();
  }
}
